// 1.2 Serveur
// Le serveur doit accepter des connexions de la part de nouveaux clients, et gérer les différents
// salons. Il doit être possible de créer ou de supprimer des salons. Lorsqu’il reçoit un message, il
// doit le réenvoyer aux bons utilisateurs. Le serveur reçoit bien évidemment plusieurs messages en
// même temps, et doit donc être capable de les traiter en parallèle.
// Le serveur est accessible en ligne de commande. Il n’y a rien de persistant sur le serveur, vous
// n’avez pas à enregistrer les messages, ni de comptes utilisateur.

